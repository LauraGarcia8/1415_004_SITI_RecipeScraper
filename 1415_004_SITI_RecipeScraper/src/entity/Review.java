package entity;

public class Review
{
	String author;
	String text;
	String stars;
	
	public String getAuthor()
	{
		return author;
	}
	public void setAuthor(String author)
	{
		this.author = author;
	}
	public String getText()
	{
		return text;
	}
	public void setText(String text)
	{
		this.text = text;
	}
	public String getStars()
	{
		return stars;
	}
	public void setStars(String stars)
	{
		this.stars = stars;
	}
	
	
}
